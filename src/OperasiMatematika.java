public class OperasiMatematika {
    public static void main(String[] args) {

        int a = 100;
        int b = 10;

        // Operasi penambahan
        System.out.println(a + b);

        // Operasi pengurangan
        System.out.println(a - b);

        // Operasi perkalian
        System.out.println(a * b);

        // Operasi pembagian
        System.out.println(a / b);

        // Operasi modulo (sisa pembagian)
        System.out.println(a % b);

        // Cara persingkatan
        int c = 100;

        // Menggunakan operator penyingkatan +=
        c += 10;
        System.out.println(c);

        // Menggunakan operator penyingkatan -=
        c -= 10;
        System.out.println(c);

        // Menggunakan operator penyingkatan *=
        c *= 10;
        System.out.println(c);

        // Contoh penggunaan operator positif (+) dan negatif (-)
        int d = 100; // Positif

        // Menggunakan operator penurunan (--)
        d--;
        System.out.println(d);

        // Menggunakan operator peningkatan (++)
        d++;
        System.out.println(d);


    }
}
